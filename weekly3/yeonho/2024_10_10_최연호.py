 # 학생들의 성적을 딕셔너리로 저장하고, 평균 점수를 계산하여 출력하는 프로그램을
 # 작성하시오. 예를 들어, {'John': 85, 'Jane': 92, 'Dave': 78}이라는 데이터가 주어지면, 
 # 이 학생들의 평균 점수를 출력하는 프로그램을 구성 하시오
 
def solution1(data):
    sum=0
    for i in data:
        sum+=int(data[i])
    return sum/len(data)


data ={'John': 85, 'Jane': 92, 'Dave': 78}
print(solution1(data))
