# 코딩테스트 연습_월간 코드 챌린지 시즌1_3진법 뒤집기
#출처_https://school.programmers.co.kr/learn/courses/30/lessons/68935

# 자연수 n이 매개변수로 주어집니다. n을 3진법 상에서 앞뒤로 뒤집은 후,
# 이를 다시 10진법으로 표현한 수를 return 하도록 solution 함수를 완성해주세요.


# @제한사항@
# n은 1 이상 100,000,000 이하인 자연수입니다.

#입출력 예

# n   | result
# -------------
# 45  | 7
# 125 | 229


# 입출력 예 #1
#--------------------------------------------------------------
# 답을 도출하는 과정은 다음과 같습니다.

# n (10진법)   \n (3진법)   \앞뒤 반전(3진법) \10진법으로 표현
#      45	    \   1200	 \      0021	     \   7
# 
# 따라서 7을 return 해야 합니다.
# 
#--------------------------------------------------------------- 
# 입출력 예 #2

# 답을 도출하는 과정은 다음과 같습니다.
# n (10진법)    \ (3진법)	   \  앞뒤 반전(3진법) \	10진법으로 표현
# 125	         \ 11122	    \   22111	        \      229


def solution3(n):
    b=""
    while n != 0:
        a = n % 3
        b = str(a)+b
        n //=3

    lst=list(b)
    lst2=list(reversed(lst))
    print(lst2)

    sum=0
    s=len(lst2)-1
    
    for i in lst2:
        sum+=(int(i)*(3**s))
        s-=1
    
    return sum



print(solution3(125))