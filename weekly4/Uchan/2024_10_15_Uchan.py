    for i in range(1, n+1) :
        if i%10==3 :  
            print("X", end=' ')#출력 후 공백문자(빈칸, ' ')로 끝냄
        elif i ==30 : #30에 도 x 로 출력
            print("X", end=' ')#출력 후 공백문자(빈칸, ' ')로 끝냄 
        else:
            print(i,end =' ')





